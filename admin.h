using namespace std; //namespaces
void sum(int no1,int no2);
void dif(int no1,int no2);
void division(int no1,int no2);
void mul(int no1,int no2);

void sum(int no1,int no2)
{
    cout<<"\nThe sum of the Two Numbers is "<<no1+no2<<"\n";
}
void dif(int no1,int no2)
{
    cout<<"\nThe Difference of the Two Numbers is "<<no1-no2<<"\n";
}
void division(int no1,int no2)
{
    cout<<"\nThe Times of the Two Numbers is "<<no1/no2<<"\n";
}
void mul(int no1,int no2)
{
    cout<<"\nThe Product of the Two Numbers is "<<no1*no2<<"\n";
}
